x = 50
if x >= 0 and x < 10:
    print(x, "falls between 0 and 10")
if x >= 10 and x < 20:
    print(x, "falls between 10 and 20")
if x >= 20 and x < 30:
    print(x, "falls between 20 and 30")
if x >= 30 and x < 40:
    print(x, "falls between 30 and 40")
if x >= 40 and x < 50:
    print(x, "falls between 40 and 50")
else:
    print(x, "is not within any range")

